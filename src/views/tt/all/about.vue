<script setup>
import { ref, onMounted, onBeforeUnmount } from 'vue'

const ourServices = ref(null)
const strategy = ref(null)

const ourServicesVisible = ref(false)
const strategyVisible = ref(false)

const heading = ref(null)
const headingVisible = ref(false)
let observer = null

onMounted(() => {
  observer = new IntersectionObserver(
    (entries) => {
      entries.forEach((entry) => {
        const isVisible = entry.isIntersecting
        if (entry.target === ourServices.value) ourServicesVisible.value = isVisible
        if (entry.target === strategy.value) strategyVisible.value = isVisible
        if (entry.target === heading.value) headingVisible.value = isVisible
      })
    },
    { threshold: 0.4 }
  )

  if (ourServices.value) observer.observe(ourServices.value)
  if (strategy.value) observer.observe(strategy.value)
  if (heading.value) observer.observe(heading.value)
})

onBeforeUnmount(() => {
  if (observer) {
    if (ourServices.value) observer.unobserve(ourServices.value)
    if (strategy.value) observer.unobserve(strategy.value)
    if (heading.value) observer.unobserve(heading.value)
  }
})
